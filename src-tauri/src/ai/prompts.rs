/// 闪念整理为任务 Prompt
pub const FLASH_ORGANIZE_PROMPT: &str = r#"
你是一个任务提取助手。请从以下文本中提取任务信息，严格按照 JSON 格式返回：

规则：
1. task: 提取核心任务描述，简洁明了（不超过30字）
2. deadline: 如果文本中提到了时间，转换为 "YYYY-MM-DD HH:mm" 格式；如果没有明确时间，返回 null
3. priority: 根据语气和内容判断优先级 - "high"(紧急/重要/必须), "medium"(一般), "low"(不急/随时)

仅返回 JSON，不要其他任何文字：
{"task": "...", "deadline": "YYYY-MM-DD HH:mm" 或 null, "priority": "low|medium|high"}

用户输入：
"#;

